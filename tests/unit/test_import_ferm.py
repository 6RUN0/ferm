"""Unit tests for :mod:`pyferm.import_ferm`.

Covers the pure helpers (``ferm_escape``, ``format_array``, the tokenizer and
the ``Data::Dumper``-equivalent ``_canon``), the two-pass ``optimize`` merge
(common-prefix block and array combine), and an end-to-end ``Importer.run``
over a small ``iptables-save`` snippet.
"""

from __future__ import annotations

import io

from pyferm.import_ferm import (
    Importer,
    MatchEntry,
    Rule,
    _canon,
    _optimize,
    _tokenize,
    ferm_escape,
    format_array,
)
from pyferm.values import Multi, Negated


def _imported(save: str) -> str:
    """Run the importer over ``save`` and return its ferm output."""
    out = io.StringIO()
    Importer(out).run(save.splitlines())
    return out.getvalue()


def test_ferm_escape_passes_bare_words() -> None:
    assert ferm_escape("eth0") == "eth0"
    assert ferm_escape("192.168.0.0/24") == "192.168.0.0/24"
    assert ferm_escape("a-b.c:d/e") == "a-b.c:d/e"


def test_ferm_escape_quotes_specials_and_empty() -> None:
    assert ferm_escape("a b") == "'a b'"
    assert ferm_escape("") == "''"
    assert ferm_escape("a,b") == "'a,b'"


def test_format_array_scalar_single_and_multi() -> None:
    assert format_array("x") == "x"
    assert format_array(["only"]) == "only"
    assert format_array(["a", "b", "c"]) == "(a b c)"
    # A blessed ``multi`` is dereferenced like a plain array.
    assert format_array(Multi(["a", "b"])) == "(a b)"


def test_tokenize_quotes_bang_and_words() -> None:
    assert _tokenize("--source 1.2.3.4 --jump ACCEPT") == [
        "--source", "1.2.3.4", "--jump", "ACCEPT",
    ]
    # A lone ``!`` splits even when glued to the next token.
    assert _tokenize("! --proto tcp") == ["!", "--proto", "tcp"]
    assert _tokenize('--comment "a b c"') == ["--comment", "a b c"]


def test_canon_distinguishes_negation_tag() -> None:
    # A negated value differs from the bare scalar and from a plain array.
    assert _canon(Negated("x")) != _canon("x")
    assert _canon(Negated("x")) != _canon(["x"])
    assert _canon(Negated("x")) == _canon(Negated("x"))


def test_optimize_factors_common_prefix_block() -> None:
    # Two rules sharing a leading match collapse into one block.
    rules = [
        Rule(match=[MatchEntry("proto", "tcp"), MatchEntry("saddr", "1")],
             jump="ACCEPT"),
        Rule(match=[MatchEntry("proto", "tcp"), MatchEntry("saddr", "2")],
             jump="DROP"),
    ]
    result = _optimize(rules)
    assert len(result) == 1
    block = result[0]
    assert block.block is not None
    assert [e.name for e in block.match] == ["proto"]
    assert len(block.block) == 2


def test_optimize_combines_array_values() -> None:
    # Two rules differing only in one scalar merge into an array value.
    rules = [
        Rule(match=[MatchEntry("saddr", "1.2.3.4")], jump="ACCEPT"),
        Rule(match=[MatchEntry("saddr", "5.6.7.8")], jump="ACCEPT"),
    ]
    result = _optimize(rules)
    assert len(result) == 1
    assert result[0].match[0].value == ["1.2.3.4", "5.6.7.8"]


def test_optimize_keeps_negated_rules_separate() -> None:
    # Negated values are not array-merging members, so they stay distinct.
    rules = [
        Rule(match=[MatchEntry("saddr", Negated("1.2.3.4"))], jump="DROP"),
        Rule(match=[MatchEntry("saddr", Negated("5.6.7.8"))], jump="DROP"),
    ]
    result = _optimize(rules)
    assert len(result) == 2


def test_run_emits_nested_blocks_and_policy() -> None:
    save = """\
# Generated by iptables-save
*filter
:INPUT ACCEPT [0:0]
-A INPUT --in-interface eth0 --jump ACCEPT
COMMIT
"""
    output = _imported(save)
    assert "domain ip {" in output
    assert "table filter {" in output
    assert "chain INPUT {" in output
    assert "policy ACCEPT;" in output
    assert "interface eth0 ACCEPT;" in output
    # Balanced braces: three opens (domain/table/chain) and three closes.
    assert output.count("{") == 3
    assert output.count("}") == 3


def test_run_negated_option_renders_bang_after_key() -> None:
    save = """\
*filter
:INPUT ACCEPT [0:0]
-A INPUT ! --source 10.0.0.0/8 --jump DROP
COMMIT
"""
    output = _imported(save)
    assert "saddr ! 10.0.0.0/8 DROP;" in output
